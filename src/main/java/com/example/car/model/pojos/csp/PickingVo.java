package com.example.car.model.pojos.csp;

public class PickingVo {
    private String commName;
    private String commSpe;
    private String commCar;
    private String commUnit;
    private double commMoney;
    private int pickingNum;

    public String getCommName() {
        return commName;
    }

    public void setCommName(String commName) {
        this.commName = commName;
    }

    public String getCommSpe() {
        return commSpe;
    }

    public void setCommSpe(String commSpe) {
        this.commSpe = commSpe;
    }

    public String getCommCar() {
        return commCar;
    }

    public void setCommCar(String commCar) {
        this.commCar = commCar;
    }

    public String getCommUnit() {
        return commUnit;
    }

    public void setCommUnit(String commUnit) {
        this.commUnit = commUnit;
    }

    public double getCommMoney() {
        return commMoney;
    }

    public void setCommMoney(double commMoney) {
        this.commMoney = commMoney;
    }

    public int getPickingNum() {
        return pickingNum;
    }

    public void setPickingNum(int pickingNum) {
        this.pickingNum = pickingNum;
    }
}
